#how fish behave
fishcycle <- function(x) ifelse(x==0,6,x-1)

fishpopulation <- function(end.time=18, init.fish=input){
        previous.gen<-2 #any non-zero, just to initiate
        for(day  in 1:end.time){
                init.fish <- fishcycle(init.fish)
                if(any(previous.gen==0)) init.fish <- c(init.fish, rep(8,sum(previous.gen==0)))
                previous.gen<-init.fish
        }
        return(init.fish)
}

